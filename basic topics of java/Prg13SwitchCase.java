class Prg13SwitchCase{
    public static void main(String args[]){
        int num = 50;

        switch(num){
            case 100 : System.out.println("case 1");
            break;
            case 300 : System.out.println("case 2");
            break;
            case 50 : System.out.println("case 3");
            break;
        }
    }
}